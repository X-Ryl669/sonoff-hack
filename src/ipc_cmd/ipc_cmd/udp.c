int udp_send(const char *host, int port, const unsigned char *msg, int size)
{
    struct sockaddr_in servaddr;
    int fd = socket(AF_INET, SOCK_DGRAM, 0);
    if (fd < 0) {
        fprintf(stderr, "Cannot open UDP socket\n");
        return -11;
    }

    bzero(&servaddr, sizeof(servaddr));
    servaddr.sin_family = AF_INET;
    servaddr.sin_addr.s_addr = inet_addr(host);
    servaddr.sin_port = htons(port);
    if (sendto(fd, msg, size, 0,
               (struct sockaddr*) &servaddr, sizeof(servaddr)) < 0) {
        fprintf(stderr, "Cannot send UDP message\n");
        close(fd);
        return -12;
    }
    close(fd);
    if (debug) fprintf(stderr, "UDP message sent\n");

    return 0;
}
